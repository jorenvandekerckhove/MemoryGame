class Grid:
    def __init__(self, min_x, min_y, max_x, max_y, width_tile, height_tile, grid_array):
        self.min_x = min_x
        self.min_y = min_y
        self.max_x = max_x
        self.max_y = max_y
        self.width_tile = width_tile
        self.height_tile = height_tile
        self.grid_array = grid_array